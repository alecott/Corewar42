#include "../includes/asm.h"

void	ft_pass_space(char *str, int *j)
{
	int i;

	i = 0;
	while (str[*j] && (str[*j] == ' ' || str[*j] == '\t'))
	{
		if (str[*j] == '\n')
			return ;
		*j = *j + 1;
	}
	*j = *j - 1;
}
